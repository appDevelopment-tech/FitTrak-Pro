async function addArmExercises() {
  console.log('Добавляем упражнения для рук...');
  
  for (const exercise of armExercises) {
    try {
      const response = await fetch('http://localhost:5000/api/exercises', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          ...exercise,
          createdBy: 1
        }),
      });
      
      if (response.ok) {
        const result = await response.json();
        console.log(`✓ Добавлено упражнение: ${exercise.name}`);
      } else {
        console.error(`✗ Ошибка при добавлении ${exercise.name}:`, await response.text());
      }
    } catch (error) {
      console.error(`✗ Ошибка при добавлении ${exercise.name}:`, error);
    }
  }
  
  console.log('Все упражнения для рук добавлены!');
}

